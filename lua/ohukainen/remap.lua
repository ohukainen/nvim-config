vim.keymap.set('n', '<leader>h', function() vim.diagnostic.show() end, { desc = 'Show diagnostic messages' })
vim.keymap.set('n', '<leader>b', function() vim.diagnostic.hide() end, { desc = 'Hide diagnostic messages' })

